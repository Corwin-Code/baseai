package com.clinflash.baseai.application.mcp.dto;


/**
 * <h2>工具使用情况统计数据</h2>
 *
 * <p>这个数据传输对象记录了单个工具的使用频率和热度信息。在MCP工具生态系统中，
 * 了解哪些工具最受欢迎、使用频率如何，对于系统优化和资源分配具有重要价值。
 * 就像商店的销售统计一样，这些数据揭示了"热销商品"，帮助管理者做出明智决策。</p>
 *
 * <p><b>应用价值：</b></p>
 * <ul>
 * <li><b>热门工具识别：</b>快速识别系统中最受欢迎的工具，为推荐算法提供数据基础</li>
 * <li><b>性能优化指导：</b>高频使用的工具需要优先进行性能优化和稳定性保障</li>
 * <li><b>资源分配依据：</b>为服务器资源、缓存策略等技术决策提供数据支撑</li>
 * <li><b>工具生态洞察：</b>了解用户偏好，指导新工具的开发方向</li>
 * </ul>
 *
 * <p><b>使用场景：</b>这个DTO主要用于仪表板展示、统计报表生成、
 * 以及管理员的运营分析界面。通过排序和聚合这些数据，
 * 可以生成"工具排行榜"、"使用趋势分析"等有价值的报告。</p>
 *
 * @param toolId    工具的唯一标识符，用于关联具体的工具实例
 * @param toolCode  工具代码，便于程序化识别和处理
 * @param toolName  工具的显示名称，用于用户界面展示
 * @param callCount 工具的调用次数，反映其受欢迎程度和使用频率
 */
public record ToolUsageDTO(
        Long toolId,
        String toolCode,
        String toolName,
        long callCount
) {
}